import UIKit

extension UIFont {
    static let smallFont = UIFont.systemFont(ofSize: 13)
    static let smallBoldFont = UIFont.systemFont(ofSize: 13, weight: .bold)
    static let mediumFont = UIFont.systemFont(ofSize: 14)
    static let mediumBoldFont = UIFont.systemFont(ofSize: 14, weight: .bold)
    static let largeFont = UIFont.systemFont(ofSize: 16)
    static let largeBoldFont = UIFont.systemFont(ofSize: 16, weight: .bold)
}
